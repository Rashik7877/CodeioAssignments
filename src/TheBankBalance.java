public class TheBankBalance {
    public static void main(String[] args) {
        int balance = 0;
        if (balance > 0){
            System.out.println("Positive Balance");
        }
        else if (balance < 0) {
            System.out.println("Negative Balance");
        }
        else {
            System.out.println("Zero Balance");
        }
    }
}